export const getReadingPlanForDate = (date) => {
    // 주어진 날짜에 해당하는 읽기 계획을 찾습니다.
    const plan = BibleReadingsData.filter(plan => plan.date === date);

    // 찾은 계획이 있다면, 해당 계획을 리턴합니다.
    if (plan && plan.length > 0) {
        return plan; // 일관된 배열 형태로 리턴
    } else {
        // 해당 날짜에 대한 계획이 없는 경우
        return [];
    }
}
